//  What is the typeof operator used for? Provide two examples.
// The typeof operator in JavaScript is used to determine the data type of a given value or variable.
//  It returns a string that represents the type of the operand.
// Examples
// 1. Checking the type of a string.
let message = "Hello students";
console.log(typeof message);

// 2. Checking the type of a number.
let age = 25;
console.log(typeof age);